package com.Java_Practice.Getters_and_Setters.Venue;

public class Venue {
    private String venue;
    private String city;

    public String getVenue() {
        return venue;
    }

    public void setVenue(String venue) {
        this.venue = venue;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
}
